type DeepStringMap<T> = {
  [K in keyof T]: T[K] extends string ? string : DeepStringMap<T[K]>;
};

export type Messages = DeepStringMap<(typeof MESSAGES)["en"]>;

export function getMessages(locale: Locale): Messages {
  return MESSAGES[locale] ?? MESSAGES.en;
}

export function detectInitialLocale(): Locale {
  if (typeof window === "undefined") return "en";
  const saved = window.localStorage.getItem("cmc-locale");
  if (saved === "ko" || saved === "en") return saved;
  const browserLocale = window.navigator.language.toLowerCase();
  return browserLocale.startsWith("ko") ? "ko" : "en";
}
